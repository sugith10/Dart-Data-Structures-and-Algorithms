/// Checking if a String is Empty in Dart
///
/// The `isEmpty` property in Dart is used to check whether a string is empty or not.
/// It returns `true` if the string has no characters, and `false` otherwise.
///
/// Example:

void main() {
  String emptyStr = "";
  
  bool isEmpty = emptyStr.isEmpty; 

  print(isEmpty); // Output: true
}
